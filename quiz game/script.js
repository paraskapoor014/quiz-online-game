let currentCategory = "";
let currentQuestionIndex = 0;
let score = 0;
let timer;
let timeLeft = 30;

function startQuiz(category) {
    currentCategory = category;
    currentQuestionIndex = 0;
    score = 0;
    timeLeft = 30;
    
    // Hide category selection and show the quiz
    document.getElementById('category-selection').style.display = 'none';
    document.getElementById('quiz').style.display = 'block';
    document.getElementById('end-screen').style.display = 'none';

    // Reset score and timer
    document.getElementById('score').innerText = score;
    document.getElementById('timer').innerText = timeLeft;

    // Start the quiz
    displayQuestion();
    startTimer();
}

function startTimer() {
    timer = setInterval(function() {
        timeLeft--;
        document.getElementById('timer').innerText = timeLeft;
        if (timeLeft <= 0) {
            clearInterval(timer);
            endQuiz();
        }
    }, 1000);
}

function displayQuestion() {
    let question = questions[currentCategory][currentQuestionIndex];
    document.getElementById('question').innerText = question.question;

    let optionsHtml = '';
    question.options.forEach(option => {
        optionsHtml += `<button class="btn" onclick="checkAnswer('${option}')">${option}</button>`;
    });
    document.getElementById('options').innerHTML = optionsHtml;
}

function checkAnswer(answer) {
    let correctAnswer = questions[currentCategory][currentQuestionIndex].correctAnswer;
    if (answer === correctAnswer) {
        score++;
        document.getElementById('score').innerText = score;
    }

    // Disable buttons after an answer is selected
    let buttons = document.querySelectorAll('#options button');
    buttons.forEach(button => button.disabled = true);

    // Show next button
    document.getElementById('next-button').style.display = 'block';
}

function nextQuestion() {
    currentQuestionIndex++;
    if (currentQuestionIndex < questions[currentCategory].length) {
        displayQuestion();
        document.getElementById('next-button').style.display = 'none';

        // Re-enable buttons for the next question
        let buttons = document.querySelectorAll('#options button');
        buttons.forEach(button => button.disabled = false);
    } else {
        clearInterval(timer);
        endQuiz();
    }
}

